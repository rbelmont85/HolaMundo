package core;

public class Coche {
	
	private int ruedas;
	
	private int puertas;

	public Coche(int ruedas, int puertas) {
		super();
		this.ruedas = ruedas;
		this.puertas = puertas;
	}

	public int getRuedas() {
		return ruedas;
	}

	public void setRuedas(int ruedas) {
		this.ruedas = ruedas;
	}

	public int getPuertas() {
		return puertas;
	}

	public void setPuertas(int puertas) {
		this.puertas = puertas;
	}
	
	

}
